package com.gamespurchase.entities;

public class TimeGame {

    private String id;

    private String hour;

    public TimeGame() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getHour() {
        return hour;
    }

    public void setHour(String hour) {
        this.hour = hour;
    }
}
